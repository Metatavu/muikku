package fi.muikku.calendar;

public enum CalendarEventRecurrenceFrequency {
  
  /**
   * The event is recurring secondly
   */
  SECONDLY,
  
  /**
   * The event is recurring minutely
   */
  MINUTELY,
  
  /**
   * The event is recurring daily
   */
  DAILY,
  
  /**
   * The event is recurring weekly
   */
  WEEKLY,

  /**
   * The event is recurring monthly
   */
  MONTHLY,
  
  /**
   * The event is recurring yearly
   */
  YEARLY
}
